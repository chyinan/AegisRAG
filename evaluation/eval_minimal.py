#!/usr/bin/env python3
"""Minimal RAG evaluation — directly calls API, gets chunk text from DB, scores with DeepSeek."""
from __future__ import annotations
import asyncio, json, os, re, time, asyncpg
from pathlib import Path
import httpx

API = "http://localhost:8000"
HDR = {
    "Content-Type": "application/json",
    "X-User-ID": "admin", "X-Tenant-ID": "default",
    "X-Roles": "admin,platform_admin",
    "X-Permissions": "document:read,retrieval:query",
}

def _load_db_url():
    """Read DATABASE_URL from project .env file."""
    env_path = Path(__file__).resolve().parent.parent / ".env"
    with open(env_path) as f:
        for line in f:
            if line.startswith("DATABASE_URL="):
                url = line.split("=", 1)[1].strip().strip('"').strip("'")
                # Rewrite asyncpg -> postgresql and use localhost
                return url.replace("+asyncpg", "").replace("postgres:5432", "localhost:5432")
    return "postgresql://rag_app:password@localhost:5432/rag_app"

DB = _load_db_url()

def _load_deepseek_key():
    env_path = Path(os.environ.get("LOCALAPPDATA", "")) / "hermes" / ".env"
    with open(env_path) as f:
        for line in f:
            if "DEEPSEEK_API_KEY" in line:
                return line.split("=", 1)[1].strip().strip('"').strip("'")
    return ""

DS_KEY = _load_deepseek_key()
DS_URL = "https://api.deepseek.com/v1/chat/completions"

QUESTIONS = [
    ("q01", "What is Retrieval-Augmented Generation (RAG)?"),
    ("q02", "How does AegisRAG handle multi-tenant access control?"),
    ("q03", "What chunking strategy does AegisRAG use?"),
    ("q04", "Explain the hybrid retrieval pipeline in AegisRAG."),
    ("q05", "How does AegisRAG ensure answer faithfulness?"),
]

FAITHFULNESS_PROMPT = """You are evaluating the faithfulness of an answer generated by a RAG system.
Faithfulness means: every factual claim in the answer MUST be directly supported by the given context documents.
If the answer makes claims not found in the context, it's unfaithful.

Question: {question}
Answer: {answer}
Context Documents:
{contexts}

Score the faithfulness on a scale of 0.0 to 10.0:
- 0-3: Major hallucination — answer contradicts context or makes up facts
- 4-6: Mixed — some claims supported, some unsupported
- 7-8: Mostly faithful — minor unsupported details
- 9-10: Perfectly faithful — everything traceable to context

Output ONLY a single number like 8.5. No other text."""

PRECISION_PROMPT = """You are evaluating the precision of context retrieval in a RAG system.
Context Precision: among all retrieved documents, how many are RELEVANT to answering the question?
Irrelevant documents in the context reduce precision.

Question: {question}
Retrieved Context Documents:
{contexts}

Score the precision on a scale of 0.0 to 10.0:
- 0-3: Most documents irrelevant to the question
- 4-6: About half relevant
- 7-8: Most documents relevant, minor noise
- 9-10: All documents are highly relevant

Output ONLY a single number like 7.0. No other text."""


async def query_rag(client: httpx.AsyncClient, question: str):
    r = await client.post(f"{API}/query", json={"query": question, "top_k": 10}, headers=HDR)
    r.raise_for_status()
    inner = (r.json().get("data", {}) or {})
    answer = re.sub(r'<thinking>.*?</thinking>', '', inner.get("answer", "") or "", flags=re.DOTALL).strip()

    r2 = await client.post(f"{API}/retrieve", json={"query": question, "top_k": 10}, headers=HDR)
    r2.raise_for_status()
    inner2 = (r2.json().get("data", {}) or {})
    candidates = inner2.get("candidates", [])
    chunk_ids = [c["chunk_id"] for c in candidates if c.get("chunk_id")]
    return answer, chunk_ids


async def get_chunk_texts(pool: asyncpg.Pool, chunk_ids: list[str]) -> list[str]:
    texts = []
    for cid in chunk_ids:
        row = await pool.fetchrow("SELECT content FROM chunks WHERE chunk_id = $1", cid)
        if row:
            texts.append(row["content"])
    return texts


async def judge(client: httpx.AsyncClient, prompt: str) -> float:
    r = await client.post(DS_URL, json={
        "model": "deepseek-v4-flash",
        "messages": [
            {"role": "system", "content": "You are an expert evaluator. Output ONLY a number, no explanation."},
            {"role": "user", "content": prompt},
        ],
        "temperature": 0,
        "max_tokens": 10,
    }, headers={"Authorization": f"Bearer {DS_KEY}", "Content-Type": "application/json"}, timeout=30)
    r.raise_for_status()
    text = r.json()["choices"][0]["message"]["content"].strip()
    try:
        score = float(text) / 10.0
        return max(0.0, min(1.0, score))
    except ValueError:
        nums = re.findall(r'[\d.]+', text)
        score = float(nums[0]) / 10.0 if nums else 0.0
        return max(0.0, min(1.0, score))


async def main():
    pool = await asyncpg.create_pool(DB, min_size=2, max_size=5)

    async with httpx.AsyncClient(timeout=60) as client:
        faith_scores, prec_scores, latencies, results = [], [], [], []

        for qid, question in QUESTIONS:
            print(f"\n{'='*60}")
            print(f"[{qid}] {question}")
            print(f"{'='*60}")

            t0 = time.perf_counter()
            answer, chunk_ids = await query_rag(client, question)
            lat = (time.perf_counter() - t0) * 1000
            latencies.append(lat)

            chunk_texts = await get_chunk_texts(pool, chunk_ids)

            print(f"Answer ({lat:.0f}ms): {answer[:200]}...")
            print(f"Contexts: {len(chunk_texts)} retrieved")
            for i, ctx in enumerate(chunk_texts[:3]):
                print(f"  [{i+1}] {ctx[:150]}...")

            ctx_text = "\n\n---\n\n".join(
                f"[Doc {i+1}]\n{c}" for i, c in enumerate(chunk_texts)
            )

            # Faithfulness
            f_prompt = FAITHFULNESS_PROMPT.format(question=question, answer=answer, contexts=ctx_text)
            faith = await judge(client, f_prompt)
            faith_scores.append(faith)
            print(f"  Faithfulness: {faith:.2f}")

            # Precision
            p_prompt = PRECISION_PROMPT.format(question=question, contexts=ctx_text)
            prec = await judge(client, p_prompt)
            prec_scores.append(prec)
            print(f"  Precision:    {prec:.2f}")

            results.append((qid, faith, prec, lat))

    await pool.close()

    print(f"\n{'='*60}")
    print("FINAL RESULTS (with LLM Reranker)")
    print(f"{'='*60}")
    print(f"{'Case':<6} {'Faithfulness':>13} {'Precision':>10} {'Latency':>8}")
    print("-" * 42)
    for qid, f, p, l in results:
        f_mark = "✅" if f >= 0.7 else "⚠️"
        p_mark = "✅" if p >= 0.5 else "⚠️"
        print(f"{qid:<6} {f_mark} {f:.2f}     {p_mark} {p:.2f}     {l:6.0f}ms")

    avg_f = sum(faith_scores) / len(faith_scores)
    avg_p = sum(prec_scores) / len(prec_scores)
    avg_l = sum(latencies) / len(latencies)
    print("-" * 42)
    print(f"{'AVG':<6}    {avg_f:.2f}        {avg_p:.2f}     {avg_l:6.0f}ms")
    print(f"\nAggregate Faithfulness: {avg_f:.4f}")
    print(f"Aggregate Precision:    {avg_p:.4f}")
    print(f"Avg Latency:            {avg_l:.0f}ms")

    report = {
        "reranker": "llm (deepseek-v4-flash)",
        "aggregate": {"faithfulness": round(avg_f, 4), "precision": round(avg_p, 4), "avg_latency_ms": round(avg_l, 0)},
        "cases": [{"id": qid, "faithfulness": round(f, 4), "precision": round(p, 4), "latency_ms": round(l, 0)}
                  for qid, f, p, l in results],
    }
    out = Path("evaluation/reports")
    out.mkdir(parents=True, exist_ok=True)
    ts = time.strftime("%Y%m%d_%H%M%S")
    (out / f"eval_{ts}.json").write_text(json.dumps(report, indent=2, ensure_ascii=False))
    print(f"\nReport: evaluation/reports/eval_{ts}.json")


if __name__ == "__main__":
    asyncio.run(main())
